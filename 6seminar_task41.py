# Дан массив, состоящий из целых чисел. Напишите программу, которая в данном массиве определит
# количество элементов, у которых два соседних и, при этом, оба соседних элемента меньше данного. Сначала
# вводится число N — количество элементов в массиве Далее записаны N чисел — элементы массива. Массив состоит из целых чисел.

def input_array():
    while True:
        try:
            n = int(input("Введите количество элементов массива: "))
            if n < 0:
                print("Пожалуйста, введите неотрицательное число.")
                continue
            lst = list(map(int, input("Введите элементы массива через пробел: ").split()))
            if len(lst) != n:
                print(f"Пожалуйста, введите ровно {n} элемента(-ов).")
                continue
            return lst
        except ValueError:
            print("Пожалуйста, введите корректные числа.")

lst_1 = input_array()

new_lst_1 = [x for i, x in enumerate(lst_1) if i > 0 and i < len(lst_1)-1 and x > lst_1[i-1] and x > lst_1[i+1]]
print("Результат:", len(new_lst_1))