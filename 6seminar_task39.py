# Даны два массива чисел. Требуется вывести те элементы первого массива (в том порядке, в каком они идут в первом
# массиве), которых нет во втором массиве. Пользователь вводит число N - количество элементов в первом массиве, затем N
# чисел - элементы массива. Затем число M - количество элементов во втором массиве. Затем элементы второго массива

def input_array():
    while True:
        try:
            n = int(input("Введите количество элементов массива: "))
            if n < 0:
                print("Пожалуйста, введите неотрицательное число.")
                continue
            lst = list(map(int, input("Введите элементы массива через пробел: ").split()))
            if len(lst) != n:
                print(f"Пожалуйста, введите ровно {n} элемента(-ов).")
                continue
            return lst
        except ValueError:
            print("Пожалуйста, введите корректные числа.")

print("ПЕРВЫЙ МАССИВ:")
lst_1 = input_array()
print("ВТОРОЙ МАССИВ:")
lst_2 = input_array()
print("Введенные массивы:", lst_1, lst_2)

new_lst_1 = [x for x in lst_1 if x not in lst_2]

print("Результат:", new_lst_1)